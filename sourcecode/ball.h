/////////////////////////////////////////////////////////////////
// Engine Includes		
#include <dj2d.h>

/////////////////////////////////////////////////////////////////
DJ_FILE_START();
/////////////////////////////////////////////////////////////////

class Ball
{
protected:
	djint32	m_ID;
	DJVector2   m_vPosition;	
	DJ2DSprite* m_pSprite;

public:
	Ball();
	~Ball();

	// Init ball
	djbool Init(djint32 id, DJVector2 vpos,DJVector2 vSize, DJString strFileName);

	// Paint ball
	void Update(djfloat fDeltaTime);

	// Term ball
	void Term();

	// ID
	djuint32 GetID() const {return m_ID;}
	void SetID(djint32 Id) {m_ID = Id;}

	// get, set position
	DJVector2 GetPosition() const {return m_vPosition;}
	void SetPosition(DJVector2 vPos) {m_vPosition = vPos;}

	// sprite
	void SetSprite(DJ2DSprite* pSprite) {m_pSprite = pSprite;} 
	DJ2DSprite* GetSprite() {return m_pSprite;}
};

